    private void btnCriarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCriarActionPerformed
        String nomeMundo = txtNome.getText().trim();
        String modo = (String) comboModo.getSelectedItem();
        if (nomeMundo.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Informe o nome do mundo.");
            return;
        }

        ServerSingle novoMundo = new ServerSingle(nomeMundo, modo, conta);
        conta.adicionarMundoSing(novoMundo);
       

        JOptionPane.showMessageDialog(this, "Mundo criado com sucesso!");
        telaPai.depoisCriarMundo();
        this.dispose();
    }//GEN-LAST:event_btnCriarActionPerformed

