    private void popularListaPlayers() {
        DefaultListModel<String> model = new DefaultListModel<>();
        List<Conta> todasAsContas = GerenciadorContas.getTodasContas();

        for (Conta c : todasAsContas) {
            if (!c.equals(conta)) {
                model.addElement(c.getNickname());
            }
        }
        listPlayers.setModel(model);
    }
    
