    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        String nomeMundo = txtNomeMundo.getText().trim();
        String modo = (String) comboModo.getSelectedItem();

        if (nomeMundo.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Informe o nome do mundo.");
            return;
        }

        List<String> jogadoresSelecionados = listPlayers.getSelectedValuesList();

        ServerMulti novoMundo = new ServerMulti(nomeMundo, modo);
        novoMundo.adicionarProprietario(conta);

        for (String nick : jogadoresSelecionados) {
            Conta jogador = GerenciadorContas.buscarPorNickname(nick);
            if (jogador != null) {
                novoMundo.adicionarProprietario(jogador);
                jogador.adicionarMundoMulti(novoMundo);
            } else {
                JOptionPane.showMessageDialog(this, "Conta " + nick + " não encontrada.");
            }
        }

        conta.adicionarMundoMulti(novoMundo);
        JOptionPane.showMessageDialog(this, "Mundo multiplayer criado!");
        telaPai.depoisCriarMundo();
        this.dispose();
    }//GEN-LAST:event_btnSubmitActionPerformed

    private void btnQuitActionPerformed(java.awt.event.ActionEvent evt) {
        telaPai.setEnabled(true);
        telaPai.toFront();
        this.dispose();
    }
    
