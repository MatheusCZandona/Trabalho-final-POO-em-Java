    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        int selectedIndex = jComboBox1.getSelectedIndex();
        if (selectedIndex < 0) {
            JOptionPane.showMessageDialog(this, "Selecione uma conta para entrar.");
            return;
        }
        Conta selecionada = GerenciadorContas.getTodasContas().get(selectedIndex);
        TelaInicial tela = new TelaInicial(selecionada);
        tela.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btnLoginActionPerformed

